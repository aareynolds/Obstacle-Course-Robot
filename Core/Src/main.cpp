double distance;

extern "C"
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	static uint32_t last_cnt = 0;
	static uint32_t second_cnt = 0;

	second_cnt = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);

	double difference;

	if (second_cnt >= last_cnt) {
		difference = second_cnt - last_cnt;
	}
	else {
		difference = (htim->Instance->ARR - last_cnt) + second_cnt;
	}

	double distance_temp = (difference * 0.25) / 58;

	if (distance_temp <= 400) {
		distance = distance_temp;
		//std::cout << distance << " cm\r\n";
	}

	last_cnt = second_cnt;
}

